    /**
     * This method checks if it's valid to insert an operator. If yes, returns
     * true. Otherwise, false.
     * @return 
     *          true if it's valid to insert an operator. Otherwise, false.
     */
    public boolean canPutOperator() {
        int size = displayTxtField.getText().length();
        int index = size - 1;
        // Returns false if the String is empty
        if (size == 0) {
            return false;
        } else {
            // Returns false if the previous char is already an operator
            if (display.charAt(index) == '+' || display.charAt(index) == '-' || 
                    display.charAt(index) == '*' || display.charAt(index) == '/'
                    || display.charAt(index) == '.') {
                return false;
            } else {
                return true;
            }
        }
    }
    
    /**
     * This method checks if it's valid to insert a decimal point. If yes, returns
     * true. Otherwise, false.
     * @return 
     *          true if it's valid to insert a decimal point. Otherwise, false.
     */
    public boolean canPutDecimalPoint() {
        int size = displayTxtField.getText().length();
        int index = size - 1;
        boolean end = false;
        boolean canPut = false;
        // Loop until it find a decimal point, an operator, or reach the start
        while (index > 0 && !end) {
            if (display.charAt(index) == '.') {
                canPut = false;
                end = true;
            } else if (display.charAt(index) == '+' || display.charAt(index) == '-' || 
                    display.charAt(index) == '*' || display.charAt(index) == '/') {
                canPut = true;
                end = true;
            }
            index--;
        }
        // Check if it reached the begining of the String, and if there was no
        // decimal point on the way.
        if (index == 0 && canPut == false && display.charAt(index) != '.') {
            canPut = true;
        }
        return canPut;
    }
    
    /**
     * Update the display text field with a new String.
     * @param str 
     *              new String
     */
    public void updateDisplay(String str) {
        display = display + str;
        displayTxtField.setText(display);
    }
    
